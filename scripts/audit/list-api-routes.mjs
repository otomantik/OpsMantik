#!/usr/bin/env node
/**
 * Lists every Next.js App Router API module (`route.ts` under `app/api`)
 * with exported HTTP methods, runtime hints,
 * and a rough client-reference count under app/ + components/ (string match).
 *
 * Usage:
 *   node scripts/audit/list-api-routes.mjs
 *   node scripts/audit/list-api-routes.mjs --write
 */
import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const ROOT = join(__dirname, '..', '..');
const API_DIR = join(ROOT, 'app', 'api');
const SCAN_DIRS = [join(ROOT, 'app'), join(ROOT, 'components')];

const write = process.argv.includes('--write');
const OUT = join(ROOT, 'docs', 'architecture', 'CLEANUP', 'API_ROUTE_INVENTORY.md');

/** @param {string} dir @param {string[]} acc */
async function walkRoutes(dir, acc = []) {
  const entries = await readdir(dir, { withFileTypes: true });
  for (const e of entries) {
    const p = join(dir, e.name);
    if (e.isDirectory()) await walkRoutes(p, acc);
    else if (e.name === 'route.ts') acc.push(p);
  }
  return acc;
}

/** @param {string} ext */
async function collectSources(ext, dirs) {
  const out = [];
  async function walk(d) {
    const entries = await readdir(d, { withFileTypes: true });
    for (const e of entries) {
      const p = join(d, e.name);
      if (e.name === 'node_modules' || e.name === '.next') continue;
      if (e.isDirectory()) await walk(p);
      else if (e.name.endsWith(ext)) out.push(p);
    }
  }
  for (const d of dirs) await walk(d);
  return out;
}

function routeUrl(abs) {
  const rel = relative(API_DIR, abs).replace(/\\/g, '/');
  return '/api/' + rel.replace(/\/route\.ts$/, '');
}

function classify(pathUrl) {
  if (pathUrl.includes('sentry-example')) return 'debug_dev';
  if (pathUrl.includes('/cron/')) return 'cron_job';
  if (pathUrl.includes('/gdpr')) return 'gdpr_compliance';
  if (pathUrl.includes('/billing')) return 'billing_finance';
  if (
    pathUrl.startsWith('/api/oci/') ||
    pathUrl.startsWith('/api/workers/google-ads-oci') ||
    pathUrl.startsWith('/api/workers/oci/') ||
    pathUrl.includes('/call-event')
  )
    return 'OCI_core';
  if (pathUrl.startsWith('/api/health')) return 'OCI_core';
  if (pathUrl.includes('tracker-embed')) return 'ingest_public';
  if (pathUrl.includes('/admin/')) return 'panel_operator';
  if (pathUrl.includes('/debug/')) return 'debug_dev';
  return 'legacy_unknown';
}

/** @param {string} text */
function analyzeHandlers(text) {
  const methods = [];
  for (const m of ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'HEAD', 'OPTIONS']) {
    if (new RegExp(`export\\s+async\\s+function\\s+${m}\\b`).test(text)) methods.push(m);
    else if (new RegExp(`export\\s+const\\s+${m}\\s*=`).test(text)) methods.push(m);
  }
  const runtimeM = text.match(/export\s+const\s+runtime\s*=\s*['"]([^'"]+)['"]/);
  const dynamicM = text.match(/export\s+const\s+dynamic\s*=\s*['"]([^'"]+)['"]/);
  return {
    methods: methods.length ? methods.join(' ') : '—',
    runtime: runtimeM?.[1] ?? 'nodejs (default)',
    dynamic: dynamicM?.[1] ?? '—',
  };
}

async function main() {
  const routes = await walkRoutes(API_DIR);
  routes.sort((a, b) => routeUrl(a).localeCompare(routeUrl(b)));

  const sourcesTsx = await collectSources('.tsx', SCAN_DIRS);
  const sourcesTs = await collectSources('.ts', SCAN_DIRS);
  const corpusFiles = [...new Set([...sourcesTsx, ...sourcesTs])].filter((f) => {
    const n = f.replace(/\\/g, '/');
    return !n.includes('/app/api/');
  });

  const bodies = await Promise.all(
    corpusFiles.map(async (f) => {
      try {
        return await readFile(f, 'utf8');
      } catch {
        return '';
      }
    }),
  );

  const lines = [
    '# API route inventory',
    '',
    '_Generated by `npm run audit:api-routes`. Do not hand-edit; re-run the script._',
    '',
    '| Route | Methods | runtime | dynamic | class | refs (app+components) |',
    '|-------|---------|---------|---------|-------|-------------------------:|',
  ];

  for (const file of routes) {
    const url = routeUrl(file);
    const text = await readFile(file, 'utf8');
    const { methods, runtime, dynamic } = analyzeHandlers(text);
    const needle = url.replace(/[[\]]/g, (c) => (c === '[' ? '\\[' : '\\]'));
    let refs = 0;
    const lit = url;
    for (const body of bodies) {
      if (!body.includes(lit)) continue;
      const re = new RegExp(needle.replace(/[.*+?^${}()|]/g, '\\$&'), 'g');
      const m = body.match(re);
      refs += m ? m.length : 0;
    }
    lines.push(`| \`${url}\` | ${methods} | ${runtime} | ${dynamic} | ${classify(url)} | ${refs} |`);
  }

  lines.push('');
  lines.push(`**Total route modules:** ${routes.length}`);
  lines.push('');

  const md = lines.join('\n');
  if (write) {
    await writeFile(OUT, md, 'utf8');
    console.error('Wrote', relative(ROOT, OUT));
  }
  process.stdout.write(md);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
